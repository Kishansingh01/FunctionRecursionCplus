#include<iostream>
using namespace std;
int main(){
    int fact(int x){
        f=1;
        for(int i=0;i<x;i++){
            f*=i;
        }
        return f;
    }
    int ncr(int n,int r);
    return fact((n)/fact(r)*fact(n-r));
    int n;
    cout<<"Enter the value of n";
    cin>>n;
    for(int i=0;i<=n;i++){
        for(int j=0;j<=n;j++){
            cout<<ncr(i,j)<<"";
                    }
        cout<<endl;
    }
}